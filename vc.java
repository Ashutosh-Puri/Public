// Calculate the volume of cylinder. (hint : Volume: π × r² × h)
// v= pi*r²*h;
public class vc
 {
	public static void main(String[] args) 
	{    
        double  r=66;
        double  h=15;
	
        double v = Math.PI*(r*r)*h;
        
        System.out.println("Volume of Cylinder :"+v);				
	}
}


